# input 2 numbers
first_num = float(input("Input the 1st number: "))
second_num = float(input("Input the 2nd number: "))

# check if number same or not
if first_num != second_num:
    # print if number same or not
    print(f"{first_num} and {second_num} are not equal")
else:
    print(f"{first_num} and {second_num} are equal")