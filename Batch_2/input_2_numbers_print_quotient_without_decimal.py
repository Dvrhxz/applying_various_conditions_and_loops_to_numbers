# input 2 numbers
first_num = float(input("Input the 1st number: "))
second_num = float(input("Input the 2nd number: "))

# divides the two numbers and result should not have decimal
quotient_no_decimal = first_num // second_num

# print the quotient
print(f"{first_num} divided by {second_num} without decimals is equal to {quotient_no_decimal}")